def enrich_dataset():
    """기존 train.json의 user 입력을 자연스러운 문장으로 변환"""
    with open(OUTPUT_DIR / "train.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    enriched = []
    used_qa = set()

    for item in data:
        user_input = item["messages"][0]["content"]
        assistant_output = item["messages"][1]["content"]

        # 상황 키워드에 매칭되는 자연스러운 입력 찾기
        new_input = user_input  # 기본값: 원본 유지
        for situation, inputs in SITUATION_INPUTS.items():
            if user_input == situation or user_input.startswith(situation):
                new_input = random.choice(inputs)
                # 가끔 캐릭터 호칭 붙이기 (30% 확률)
                if random.random() < 0.3 and new_input[0] not in "데마":
                    prefix = random.choice(["데비야 ", "마를렌 ", ""])
                    new_input = prefix + new_input
                break

        enriched.append({
            "messages": [
                {"role": "user", "content": new_input},
                {"role": "assistant", "content": assistant_output},
            ]
        })

    # EXTRA_QA 추가
    for q, a in EXTRA_QA:
        enriched.append({
            "messages": [
                {"role": "user", "content": q},
                {"role": "assistant", "content": a},
            ]
        })

    random.shuffle(enriched)

    # 저장
    with open(OUTPUT_DIR / "train.json", "w", encoding="utf-8") as f:
        json.dump(enriched, f, ensure_ascii=False, indent=2)

    print(f"=== 변환 완료 ===")
    print(f"기존 데이터: {len(data)}개")
    print(f"추가 Q&A: {len(EXTRA_QA)}개")
    print(f"최종: {len(enriched)}개")

    # 샘플 확인
    print("\n=== 랜덤 샘플 ===")
    for item in random.sample(enriched, 8):
        u = item["messages"][0]["content"]
        a = item["messages"][1]["content"]
        print(f"  user: {u}")
        print(f"  asst: {a}")
        print()


if __name__ == "__main__":
    enrich_dataset()
